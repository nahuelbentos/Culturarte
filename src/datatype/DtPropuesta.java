package datatype;

public class DtPropuesta {
	private String titulo;
	private String nickname;
	
	public DtPropuesta(String titulo, String nickname) {
		super();
		this.titulo = titulo;
		this.nickname = nickname;
	}
	
	public String getTitulo() {
		return titulo;
	}
	public String getNickname() {
		return nickname;
	}	
}
